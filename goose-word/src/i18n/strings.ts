export type MessageKey = keyof (typeof MESSAGES)["en"];

export const gooseWordCatalogs = MESSAGES;

export const LOCALE_CODES = ["en", "zh-Hans", "ja", "ko"] as const;

export const isLocaleCode = (value: string): value is LocaleCode =>
  (LOCALE_CODES as readonly string[]).includes(value);

const lookupMessage = (locale: LocaleCode, key: MessageKey): string => {
  if (locale === "zh-Hans") {
    return MESSAGES["zh-Hans"][key];
  }
  if (locale === "ja") {
    return MESSAGES.ja[key];
  }
  if (locale === "ko") {
    return MESSAGES.ko[key];
  }
  return MESSAGES.en[key];
};

export function buildTranslateFn(
  locale: LocaleCode,
): (key: MessageKey, params?: Record<string, string | number>) => string {
  return (key, params) => {
    let message = lookupMessage(locale, key);
    if (params !== undefined) {
      for (const [paramKey, paramValue] of Object.entries(params)) {
        message = message.replaceAll(`{${paramKey}}`, String(paramValue));
      }
    }
    return message;
  };
}

export function translate(
  locale: LocaleCode,
  key: MessageKey,
  params?: Record<string, string | number>,
): string {
  return buildTranslateFn(locale)(key, params);
}
