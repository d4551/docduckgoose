export type MessageKey = keyof (typeof MESSAGES)["en"];

export function translate(locale: LocaleCode, key: MessageKey): string {
  if (locale === "zh-Hans") {
    return MESSAGES["zh-Hans"][key];
  }
  if (locale === "ja") {
    return MESSAGES.ja[key];
  }
  if (locale === "ko") {
    return MESSAGES.ko[key];
  }
  return MESSAGES.en[key];
}

export function buildTranslateFn(locale: LocaleCode): (key: MessageKey) => string {
  return (key) => translate(locale, key);
}
